package com.strings;

public class WrapperClass {

	public static void main(String[] args) {
		Integer i = Integer.valueOf(7);//way to create a wrapper class
		Integer j = Integer.valueOf(8);
		System.out.println(i == j);//comparing reference variables
		int max = Integer.MAX_VALUE;//returning max value integer can hold
		System.out.println(max);




	}

}
